public class ArrayTwo {
    public static void main(String[] args) {

        String[] fruits = new String[5];

        fruits[0] = "Apple";
        fruits[1] = "Banana";
        fruits[2] = "Orange";
        fruits[3] = "Grapes";
        fruits[4] = "Woodapple";

        for (int i = 0; i < fruits.length; i++) {
            System.out.println(fruits[i]);
        }
    }
}
