public class SwitchStatement {
    public static void main(String[] args) {
        String day = "Friday";

        switch (day) {
            case "Sunday":
                System.out.println("It is Sunday");
                break;
            case "Friday":
                System.out.println("It is fucking Friday");
                break;
            case "Monday":
                System.out.println("It is fucking Monday");
        }
    }
}